class FlightData:
    def __init__(self, fly_from, fly_to, date_from, date_to, return_from, return_to, price=""):
        self.fly_from = fly_from
        self.fly_to = fly_to
        self.dateFrom = date_from
        self.dateTo = date_to
        self.return_from = return_from
        self.return_to = return_to
        self.price = price


